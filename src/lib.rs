/// Конфигурация сервера.
///
/// Загружается из переменных окружения или файла `.env`.
/// Содержит настройки порта, хоста, логирования и других параметров.
///
/// # Пример
///
/// ```rust
/// use signaling_server::Config;
///
/// let config = Config::from_env()
///     .expect("Не удалось загрузить конфигурацию");
/// ```
pub use config::Config;

/// Глобальный тип ошибки приложения.
///
/// Объединяет ошибки всех слоёв:
/// - `DomainError` — нарушения бизнес-логики
/// - `HandlerError` — ошибки обработки команд
/// - `InfraError` — проблемы с хранилищем/внешними системами
/// - `TransportError` — ошибки сетевого уровня
///
/// Все публичные функции возвращают `Result<T, AppError>`.
pub use error::AppError;

// ─────────────────────────────────────────────────────────────
// Domain: публичные сущности и сообщения
// ─────────────────────────────────────────────────────────────

/// Сообщение от клиента к серверу.
///
/// Используется для десериализации входящих WebSocket-сообщений.
/// Поддерживает команды управления комнатами и WebRTC-сигналы.
///
/// # Формат JSON
///
/// ```json
/// {
///   "type": "join_room",
///   "room_id": "room-123",
///   "participant_id": "user-456"
/// }
/// ```
pub use domain::message::ClientMessage;

/// Сообщение от сервера к клиенту.
///
/// Используется для сериализации ответов и событий.
/// Включает подтверждения, ошибки и ретрансляцию WebRTC-данных.
///
/// # Формат JSON
///
/// ```json
/// {
///   "type": "offer",
///   "from": "user-456",
///   "sdp": "v=0\r\no=-..."
/// }
/// ```
pub use domain::message::ServerMessage;

/// Информация об участнике комнаты.
///
/// Возвращается в ответах на запросы списка участников.
/// Содержит только публичные данные (без внутренних полей).
#[derive(Debug, Clone)]
pub use domain::participant::ParticipantInfo;

// ─────────────────────────────────────────────────────────────
// Infrastructure: абстракции и реализации
// ─────────────────────────────────────────────────────────────

/// Трейт репозитория комнат.
///
/// Определяет интерфейс для хранения и получения данных о комнатах.
/// Позволяет заменять реализацию хранилища без изменения бизнес-логики.
///
/// # Реализации
///
/// - [`MemoryRoomStore`] — in-memory хранилище на основе DashMap (по умолчанию)
/// - `RedisRoomStore` — (планируется) хранилище в Redis для горизонтального масштабирования
///
/// # Пример использования в тестах
///
/// ```rust
/// use signaling_server::RoomRepository;
///
/// // Можно создать мок для тестирования обработчиков
/// fn test_handler<R: RoomRepository>(store: R) { ... }
/// ```
pub use infrastructure::room_repository::RoomRepository;

/// In-memory реализация [`RoomRepository`].
///
/// Использует [`DashMap`](https://docs.rs/dashmap) для потокобезопасного
/// хранения данных в памяти. Подходит для разработки, тестирования
/// и небольших продакшен-развёртываний.
///
/// # Пример
///
/// ```rust
/// use signaling_server::{MemoryRoomStore, RoomRepository};
///
/// let store = MemoryRoomStore::new();
/// // store.insert(room).await?;
/// ```
pub use infrastructure::memory_room_store::MemoryRoomStore;

// ─────────────────────────────────────────────────────────────
// Orchestrator: главный компонент
// ─────────────────────────────────────────────────────────────

/// Оркестратор соединений.
///
/// Координирует обработку WebSocket-подключений:
/// 1. Принимает входящие сообщения
/// 2. Маршрутизирует их к соответствующим обработчикам
/// 3. Управляет состоянием соединения (рукопожатие, в комнате, закрыто)
/// 4. Обрабатывает ошибки и отправляет ответы клиентам
///
/// # Жизненный цикл
///
/// ```text
/// new() → run() → [обработка сообщений] → завершение при разрыве соединения
/// ```
///
/// # Пример
///
/// ```rust
/// use signaling_server::{Orchestrator, MemoryRoomStore};
///
/// let store = MemoryRoomStore::new();
/// let orchestrator = Orchestrator::new(store);
/// // orchestrator.handle_message(...).await?;
/// ```
pub use orchestrator::Orchestrator;

