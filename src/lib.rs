//! Модульный WebRTC Signaling Server на Rust.
//!
//! # Архитектура
//!
//! Проект следует принципам Clean Architecture:
//! - [`domain`] — сущности и бизнес-правила (чистый Rust, без внешних зависимостей)
//! - [`handlers`] — обработка команд и событий (применение доменной логики)
//! - [`infrastructure`] — адаптеры к внешним системам (хранилища, БД)
//! - [`transport`] — работа с протоколами (WebSocket)
//! - [`orchestrator`] — управление жизненным циклом соединений
//!
//! # Быстрый старт
//!
//! ```rust
//! use signaling_server::{Config, Orchestrator, MemoryRoomStore};
//!
//! # async fn example() -> Result<(), signaling_server::AppError> {
//! // 1. Загружаем конфигурацию из переменных окружения
//! let config = Config::from_env()?;
//!
//! // 2. Создаём хранилище (можно заменить на Redis, Postgres и т.д.)
//! let store = MemoryRoomStore::new();
//!
//! // 3. Инициализируем оркестратор — главный компонент сервера
//! let orchestrator = Orchestrator::new(store);
//!
//! // 4. Запускаем сервер (упрощённо)
//! // orchestrator.run(config).await?;
//! # Ok(())
//! # }
//! ```
//!
//! # Обработка ошибок
//!
//! Все публичные функции, которые могут завершиться с ошибкой, возвращают
//! [`AppError`]. Этот тип объединяет ошибки всех слоёв приложения.

